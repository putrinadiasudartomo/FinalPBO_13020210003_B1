public void tampildata(){

    try {
        Connection conn = konekk.koneksi();
         String sql = "SELECT * FROM camera";
          PreparedStatement pst = conn.prepareStatement(sql);
           ResultSet rs = pst.executeQuery(sql);
        while(rs.next()){
            model.addColumn(new Object[]{
                rs.getString(1),
                 rs.getString(2),
                  rs.getString(3),
                   rs.getString(4),
                    rs.getString(5),
                     rs.getString(6)});
        }            
            tabel1.setModel(model);
        
        
        
    } catch (Exception e) {
    }
}

public void tampiltabel(){
    DefaultTableModel model = new DefaultTableModel();
    
     model.addColumn("Nama");
      model.addColumn("Alamat");
       model.addColumn("No Identitas");
        model.addColumn("Jam Ambil");
         model.addColumn("Tanggal Ambil");
          model.addColumn("Tanggal Kembali");
    
    try {
        String sql = "select * from camera";
        Connection conn = konekk.koneksi();
        Statement stm = conn.createStatement();
        ResultSet res = stm.executeQuery(sql);
        
        while(res.next()){
            model.addRow(new Object[]{res.getString(1),res.getString(2),res.getString(3),res.getString(4),res.getString(5),res.getString(6)});
            
            tabel1.setModel(model);
        }
    } catch (Exception e) {
        System.out.println("ERROR");
    }
    
}

public void reset(){
    DefaultTableModel tbl = (DefaultTableModel) tabel1.getModel();
    
    for (int i = 0; i <= tabel1.getRowCount(); i++) {
        tbl.removeRow(all);
    }
}
