    private void btnsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsimpanActionPerformed
         try {
            Connection conn = konekk.koneksi();
            int baris = tabel1.getRowCount();
            String sql = "INSERT INTO camera VALUES ('"
                    + nama.getText()+"','"
                    + alamat.getText()+"','"
                    + noidentitas.getText()+"','"
                    + jamambil.getText()+"','"
                    + tanggalambil.getText()+"','"
                    + tanggalkembali.getText()+"')";
            
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            pst.close();
            JOptionPane.showMessageDialog(null, "berhasil disimpan");
            loadData();
            clear();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "gagal disimpan");
        }
    }//GEN-LAST:event_btnsimpanActionPerformed

    private void btneditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneditActionPerformed
         try{
            Connection conn = konekk.koneksi();
            String n = nama.getText();
            String nam = alamat.getText();
            String j = noidentitas.getText();
            String al = jamambil.getText();
            String em = tanggalambil.getText();
            String no = tanggalkembali.getText();
            String sql = "update camera set nama= '"+n
                    +"', alamat= '"+nam
                    +"',noidentitas = '"+al
                    +"',jamambil= '"+j
                    +"',tanggalambil= '"+em
                    +"',tanggalkembali = '"+no
                    +"' WHERE nama='"
                    +n
                            + "'";
            PreparedStatement pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Proses Edit data berhasil");
            loadData();
            clear();
        }catch(Exception e){
           JOptionPane.showMessageDialog(null,"error : "+e.getMessage());
        }
    }//GEN-LAST:event_btneditActionPerformed

